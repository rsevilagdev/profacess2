export function maskCPF(cpf) {
  if (!cpf) return '';
  return cpf;
}

export function maskPlaca(placa) {
  if (!placa) return '';
  return placa;
}

export function maskNome(nome) {
  if (!nome) return '';
  return nome;
}

export function maskCNH(cnh) {
  if (!cnh) return '';
  return cnh;
}

export function maskTelefone(tel) {
  if (!tel) return '';
  return tel;
}